package com.example;

public class Operators {
    public static void main(String[] args) {

        System.out.println(1+1);
        System.out.println(1-1);
        System.out.println(1*1);
        System.out.println(1/1);

        System.out.println(1==1);
        System.out.println(1!=1);
        System.out.println(1==1 && 2!=2);
        System.out.println(1==1 || 2!=2);

        int a=12;
        int b=a+1;

        System.out.println(a);
        System.out.println(b);



    }
}
